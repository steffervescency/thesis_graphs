navOpen = false;

function toggleNav() {
  arrow = document.getElementById("settings_arrow");
  var settings = document.getElementById("settings");
  if(navOpen){
    var width = "0px";
    navOpen = false;
    arrow.innerHTML = "▶"
    settings.style.display = "none"
  }
  else{
    var width = "200px";
    navOpen = true;
    arrow.innerHTML = "◀"
    setTimeout(function(){
      settings.style.display = "inline"
    }, 150);
    
  }
  document.getElementById("settings_panel").style.width = width;
  document.getElementById("settings_arrow").style.marginLeft = width;
  document.getElementById("main").style.marginLeft = width;
}

