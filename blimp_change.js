function updateExampleSentences(task){
  example_sentences_div = document.getElementById("example_sentences");

  example_sentences_div.innerHTML = "";

  sents = examples["blimp_" + task];

  x = document.createElement("span")
  x.innerHTML = "[x]"
  x.classList.add("x")
  x.onclick = function(){
    this.parentElement.style.display = "none";
  }

  example_sentences_div.appendChild(x)

  var htmloutput = "<b>" + task + "</b>" + "<br/><br/>"

  htmloutput += "<span class='good_sent'>" + sents[0]["good"] + "</span><br />"
  htmloutput += "<span class='bad_sent'>" + sents[0]["bad"] + "</span><hr />"

  htmloutput += "<span class='good_sent'>" + sents[1]["good"] + "</span><br />"
  htmloutput += "<span class='bad_sent'>" + sents[1]["bad"] + "</span>"

  example_sentences_div.insertAdjacentHTML('beforeend', htmloutput);
  example_sentences_div.style.display = "inline";

}


function makeGraphs(dataset, graph_function) {

  graphsDiv = document.getElementById("graphs");
  graphsDiv.innerHTML = "";


  for (const [key, value] of Object.entries(examples)) {
    task_id = key.substring(6)

    // add element to DOM
    div = document.createElement("div");
    div.classList.add("table_graph")
    div.classList.add("medium")

    img = document.createElement("img")
    img.src = "change_png/" + task_id + ".png"
    img.id = task_id

    img.onclick = function(){
      updateExampleSentences(this.id);
    }

    div.appendChild(img);

    graphsDiv.appendChild(div);
  }

}


